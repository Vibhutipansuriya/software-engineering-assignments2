"""
How Do You Check The Presence Of A Key In A Dictionary?
"""
inp_dict = {'Python': "A", 'Java':"B", 'Ruby':"C", 'Kotlin':"D"} 
 
search_key = 'kotlin'
 
if search_key in inp_dict: 
        print("The key is present.") 
          
else: 
        print("The key does not exist in the dictionary.") 
        