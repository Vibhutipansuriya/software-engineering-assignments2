"""Write a Python program to find the repeated items of a tuple.
"""
# Create a tuple containing a sequence of numbers
tuplex = 3, 8, 5, 6, 2, 3, 8, 8, 7
# Print the contents of the 'tuplex' tuple
print(tuplex)

# Count the number of times the value 8 appears in the 'tuplex' tuple
count = tuplex.count(8)
# Print the count of how many times 8 appears
print(count) 