"""Write a Python program to convert a list to a tuple.
"""
#Create a list containing a sequence of numbers
list_a = [4, 30, 73, 54, 5, 33]
# Print the contents of the 'list_a' list
print(list_a)

# Use the 'tuple()' function, a built-in Python function, to convert the 'list_a' list to a tuple
tuplex = tuple(list_a)
# Print the contents of the 'tuplex' tuple
print(tuplex)