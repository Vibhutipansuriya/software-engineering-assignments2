# Write a Python function to get the largest number, smallest number and sum of all from a list?

l=[100,200,500,600,400,300]  # initialization

print("Greatest number is : ", max(l))  # print the greatest element
print("Smallest number is : ", min(l))  # print the smallest element

print("The sum of the greatest and smallest number is : ", max(l)+min(l))  # adding the greatest and smallest elements 