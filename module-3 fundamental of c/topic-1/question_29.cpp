//WAP TO CONVERT MINUTES INTO SECONDS AND HOURS//

#include<stdio.h>
int main()
{
	int min,sec,hour;
	
	//input of minutes//
	printf("enter the minutes:");
	scanf("%d",&min);
	
	// output //
	
	second=minute*60;
	hour=minute/60;
	//second will be printed//
	printf("second:%d\n",sec);
	//hour will be printed//
	printf("hour:%d",hour);
	
}
